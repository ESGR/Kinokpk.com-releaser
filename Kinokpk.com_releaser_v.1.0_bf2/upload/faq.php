<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="6"></a> <b>А что такое мой рейтинг (ratio) [только для торрент-части]?</b><br />
Ратио, оно же Рейтинг, оно же Соотношение - это количество скачанной вами информации деленное на количество отданной.
Ваше ратио(соотношение) написано сверху на панели информации, рядом с вашим ником.
<br />
Важно различать общий рейтинг (ratio) и рейтинг каждого торрента, который вы скачиваете или раздаёте.
Общий рейтинг рассчитывается из общих значений загруженного и розданного вашим аккаунтом с того момента, как вы зарегистрировались на сайте.
Индивидуальный рейтинг каждого торрента учитывает только количество загруженного/скачанного для конкретного файла (торрента).<br />
<br />
Так же возможны 2 обозначения вместо рейтинга: &quot;Inf.&quot;,
это аббревиатура от слова Infinity(бесконечность), и означает, что вы скачали 0 байт, в то время, как раздали не нулевое значение;
а так же может встретиться &quot;---&quot;,
которое должно читаться как "недоступно", и означает что вы ничего не скачали и не загрузили.</a><br /><br />

