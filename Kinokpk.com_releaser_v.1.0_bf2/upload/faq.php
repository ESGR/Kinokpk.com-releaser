<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="10"></a> <b>Мой IP-адрес динамический. Что мне сделать, чтобы быть подключенным?</b><br />

Вам не нужно ничего делать. Всё что вам нужно - это убедиться, что вы вошли (залогинились)
с текущим IP- адресом, когда стартуете новую торрент-сессию (начинаете новую загрузку/раздачу).
После этого, даже если ваш IP поменяется в течении сессии, скачивание или раздача продолжатся, и статистика обновится автоматически.</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="11"></a> <b>Почему написано, что я не могу подключиться? (В графе Порт мой порт красного цвета)(И о чем мне надо позаботиться?)</b><br />
Трекер обнаружил, что у вас файрвол (firewall) или NAT, и вы не можете принимать подключения.
<br />
<br />
Это означает, что другие участники не смогут коннектиться к вам, лишь только вы к ним.
<br />
<br />
Для решения данной проблемы откройте порты, используемые для входящих соединений (такие же, как и в установках вашего клиента)
в файрволе и/или настройте ваш NAT сервер.
(Обратитесь к документации к вашему роутеру или на форум производителя).
Так же вы можете найти нужную информацию на ресурсе: <a class="altlink" href="redir.php?url=http://portforward.com/">PortForward</a>).</a><br /><br />
