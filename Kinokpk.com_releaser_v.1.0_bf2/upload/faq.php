<h2>Как я могу увеличить мою скорость?</h2>
<table width="100%" cellspacing="0" cellpadding="3"><tr><td>
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="45"></a> <b>Не набрасывайтесь на новые торренты</b><br />

Особенно, если у вас медленная скорость. Дайте скачать в первую очередь людям с широкими каналами,
которые потом будут раздавать это, в том числе и вам. Наилучшее время для скачивания находится примерно в середине жизни торрента, именно в этот момент SLR достигает своего апогея,
и вы можете качать с максимальной скоростью. (Однако в этом случае у вас не будет возможности раздавать так долго,
как если бы вы скачали данный файл с самого начала. Вот вам задачка: необходимо балансировать между этими 2мя критериями :)</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="46"></a> <b>Настройте свои железки на нормальное подключение</b><br />
Смотрите пункт <i>Почему написано, что я не могу подключиться? (В графе Доступен написано Нет)(И о чем мне надо позаботиться?)</i></a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="47"></a> <b>Ограничьте свою скорость раздачи</b><br />
Скорость раздачи может негативно сказываться на скорости загрузки в двух случаях:<br />
<ul>
    <li>Торрент участники имеют тенденции к тому, чтобы поощрять тех, кто им раздаёт.
    Это означает что если А и Б скачивают один и тот же файл, и А отсылает данные Б с высокой скоростью,
    тогда Б будет стараться поделиться тоже. Таким образом высокая скорость раздачи ведёт к высокой скорости загрузки.
    </li>

    <li>Однако, когда А загрузил что-то с Б, он должен сказать Б, что посланные данные были успешно получены.
    (Это называется подтверждения(acknowledgements) - ACKs - это один из видов сообщения "получено!").
    Если А не смог отправить такой ответ Б, тогда он (Б) приостановит раздачу ему (А) и будет ждать.
    Если А раздаёт на полной скорости может случиться так, что подтверждения
    (ACKs) будут задерживаться. Таким образом раздача на полной скорости приведёт к снижению скорости загрузки.</li>
</ul>

Наилучшего результата вы достигнете, балансируя между этими 2мя пунктами.
Скорость аплоада должна быть максимально высокой, при которой ACKs проходят без задержек.
<b>Наилучший вариант - это ограничить вашу скорость аплоада до 80% от теоретически возможной.</b>
Однако вам может понадобиться более точная настройка, которая будет наилучшим вариантом именно в вашем случае.
(Помните, что поддержание высокй скорости раздачи положительно сказывается на вашем рейтинге(ratio)).<br />
<br />
Некоторые клиенты (напр. Azureus) ограничивают общую скорость раздачи, другие (напр. Shad0w`s) позволяют ограничить каждый торрент.
Изучите ваш клиент, и задумывайтесь о скорости аплоада, в то время, как вы используете свой канал для чего-то ещё (серфинг, фтп и т.д.).</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="48"></a> <b>Ограничьте количество одновременных соединений</b><br />

Некоторые операционные системы (такие как Windows 9x) не очень хорошо переваривают большое количество соединений,
и даже могут повиснуть. Так же, некоторые домашние роутеры (особенно когда запущен NAT и/или файрвол в режиме он-лайн сканирования)
могут снижать скорость или зависать когда слишком много соединений. Не существует единого работоспособного значения, однако вы можете
попробовать установить, экспериментальным путём, количество одновременных соединений в пределах от 60 до 100.
Необходимо учитывать, что это значение суммируемое, т.е. если у вас запущено несколько клиентов,
количество соединений всего будет равно сумме соединений каждого из них.</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="49"></a> <b>Ограничьте количество одновременных раздач</b><br />
А разве это не то же самое, о чем сказано выше? Нет. Ограничение количества соединений означает ограничение количества участников,
с которыми ваш клиент контактирует и/или с которых скачивает. Ограничение раздачь подразумевает ограничение количества участников,
которым вы раздаёте. Идеальное значение этого параметра обычно намного ниже,
чем количество одновременных соединений, и непосредственно зависит от скорости вашего (физического) подключения.</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="50"></a> <b>Подождите немного</b><br />
Как описано выше, другие участники стараются в первую очередь делиться с теми, кто им раздаёт.
Когда вы только начинаете скачивать новый файл, у вас нечего предложить другим участникам, и они будут игнорировать вас.
Это приведёт к тому, что в начале загрузки скорость будет достаточно низкой, особенно, если не соеденены ни с одним или с очень
малым количеством сидеров.
Скорость загрузки должна увеличиться как только у вас появятся несколько частей для раздачи.</a><br /><br />
<a href="#top"><img src="pic/arrowup.gif" border="0" /></a><a name="51"></a> <b>Почему страницы так медленно открываются, когда я качаю что-то?</b><br />
Ваша скорость загрузки имеет конечное значение (зависит от вашего провайдера, тарифа и т.д. и т.п.).
Если вы участник быстрого торрента это привидёт к тому, что ваш канал будет загружен по максимуму,
и соответсвенно серфинг будет очень медленным. Однако, вы можете ограничить скорость загрузки в вашем клиенте
(например Azureus позволяет это сделать)*.
А так же вы можете использовать другие программы для ограничения загрузки канала
определённой программой, например при помощи <a class="altlink" href="http://anonym.to/?http://www.netlimiter.com/">NetLimiter</a>.<br />

<br />
(Серфинг взят просто для примера, та же самая картина будет наблюдаться при игре через интернет, скачивании чего-либо по http/ftp, и т.д.)</a><br /><br />
